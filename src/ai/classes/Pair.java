package ai.classes;

public class Pair {
	
	Gene A;
	
	Gene B;
	
	int generation;

	public Gene getA() {
		return A;
	}

	public Gene getB() {
		return B;
	}

	public void setA(Gene a) {
		A = a;
	}

	public void setB(Gene b) {
		B = b;
	}

	public int getGeneration() {
		return generation;
	}

	public void setGeneration(int generation) {
		this.generation = generation;
	}
	
	

}
